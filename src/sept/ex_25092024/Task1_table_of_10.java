package sept.ex_25092024;

public class Task1_table_of_10 {
    public static void main(String[] args) {
        /* Table of number n = 10, print table with For or while.
        int n= 10;
        for (int i=1; i<=10; i++){
            System.out.println(n+" * "+i+" = "+n*i);
        }*/
         int n= 10;
         int i= 1;
         while (i<=10){
             System.out.println(n+" * "+i+" = "+n*i);
             i++;
         }


    }
}
