package sept.ex_16092024;

public class Lab031_Arithmatic_operators {
    public static void main(String[] args) {

        // + - / * and % are Arithmatic operators.
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        // % modulas - it gives remainder 0

    }
}
